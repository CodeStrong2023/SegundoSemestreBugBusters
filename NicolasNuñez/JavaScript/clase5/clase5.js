
//Tipo de dato Undefined

let x;
x = undefined; //se asigna undefined al no darle un valor a la variable
console.log(x)
console.log(typeof x)

//null --> es la ausencia de un valor

let y = null; //null no es un tipo de dato, pero su origen es de tipo object
console.log(y)
console.log(typeof y)

//Tipo de dato Array --> son de tipo object

let autos = ["Peugeot", "Fiat", "Chevrolet", "BMW"]
console.log(autos)
console.log(typeof autos)

//Empty String
let z = ""; //Es una cadena vacia
console.log(z)
console.log(typeof z)