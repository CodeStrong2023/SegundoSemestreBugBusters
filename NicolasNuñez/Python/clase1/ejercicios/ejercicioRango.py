# Ejercicios Rangos

# Ejercicio 1: Iterar in rango de 0 a 10 e imprimir numeros divisibles entre 3

print("Rango de 0 a 10 con números divisibles entre 3")
for i in range(11):
    if(i % 3 == 0):
        print(i)

# Ejercicio 2: Crear un rango de números de 2 a 5 e imprimirlos

print("Rango de 2 a 6")
for i in range(2,7):
    print(i)

# Ejercicio 3: Crear un rango de 3 a 10 pero con incremento de 2 en 2

print("Rango de 3 a 10 con incremento de 2 en 2")
for i in range(3,11,2):
    print(i)