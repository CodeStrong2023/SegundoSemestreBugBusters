
# Ejercicio 1: Iterar un rango de 0 a 10 e imprimir numeros divisibles entre 3 (ejemplo: 0,3,6,9)

x = range(0, 10, 3)
print('Rango de numeros del 0 al 10 divisibles por 3')
for n in x:
    print(n)

